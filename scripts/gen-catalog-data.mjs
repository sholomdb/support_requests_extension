/**
 * Regenerates extension/shared/catalog-data.js from extension/catalogs/*.txt
 * (one file per budget: item name line, then a "מחיר מירבי X בש"ח" line, blank line).
 * Run from the repo root:  node scripts/gen-catalog-data.mjs
 */
import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const CAT_DIR = path.join(ROOT, 'extension/catalogs');
const OUT = path.join(ROOT, 'extension/shared/catalog-data.js');

// Filename budget -> canonical BUDGET_LABELS value (only where they differ).
const BUDGET_ALIAS = { 'שלמ': 'של"מ' };

const normalizeText = (v) => String(v ?? '').trim().replace(/\s+/g, ' ');
const isPriceLine = (l) => l.startsWith('מחיר מירבי');

function budgetFromFilename(fname) {
  const base = fname.replace(/\.txt$/i, '').replace(/^קטלוג\s+/, '').trim();
  return BUDGET_ALIAS[base] || base;
}

function parsePrice(line) {
  const m = line.replace(/[,\s]/g, '').match(/(\d+)/);
  return m ? Number(m[1]) : null; // null = item exists but has no מחיר מירבי limit
}

const catalog = {};
for (const fname of readdirSync(CAT_DIR)) {
  if (!fname.endsWith('.txt')) continue;
  const budget = budgetFromFilename(fname);
  const lines = readFileSync(path.join(CAT_DIR, fname), 'utf8')
    .split(/\r?\n/)
    .map((l) => l.trim())
    .filter((l) => l.length);

  const items = {};
  let i = 0;
  while (i < lines.length) {
    const name = lines[i];
    if (isPriceLine(name)) { i += 1; continue; } // orphan price line, skip
    if (i + 1 < lines.length && isPriceLine(lines[i + 1])) {
      items[normalizeText(name)] = parsePrice(lines[i + 1]);
      i += 2;
    } else {
      i += 1; // item without a following price line, skip
    }
  }
  catalog[budget] = items;
}

const budgets = Object.keys(catalog).sort();
let body = '';
for (const b of budgets) {
  const names = Object.keys(catalog[b]).sort();
  body += `  ${JSON.stringify(b)}: {\n`;
  for (const n of names) body += `    ${JSON.stringify(n)}: ${catalog[b][n]},\n`;
  body += `  },\n`;
}

const out = `/**
 * Per-budget item catalog: budget label -> { item display name: max price (מחיר מירבי) }.
 * A null price means the item is listed but has no limit (do not split).
 *
 * GENERATED by scripts/gen-catalog-data.mjs from extension/catalogs/*.txt - do not edit
 * by hand; edit the .txt files and re-run the generator.
 */
export const CATALOG = {
${body}};

function norm(v) {
  return String(v ?? '').trim().replace(/\\s+/g, ' ');
}

/** All distinct item names across every budget (for the item category picklist). */
export function catalogItemNames() {
  const seen = new Set();
  const out = [];
  for (const items of Object.values(CATALOG)) {
    for (const name of Object.keys(items)) {
      const key = name.toLowerCase();
      if (!seen.has(key)) { seen.add(key); out.push(name); }
    }
  }
  return out.sort((a, b) => a.localeCompare(b, 'he'));
}

/** Is this budget label a known catalog budget? */
export function isCatalogBudget(budgetLabel) {
  return Boolean(CATALOG[norm(budgetLabel)]);
}

/** Does the given item exist in the given budget's catalog? (A listed item may have
 * a null price - it exists but has no מחיר מירבי limit, so key presence is the test.) */
export function budgetHasItem(budgetLabel, itemName) {
  const items = CATALOG[norm(budgetLabel)];
  return Boolean(items) && Object.prototype.hasOwnProperty.call(items, norm(itemName));
}

/** Max price (מחיר מירבי) for an item in a budget, or null if the item has no limit
 * or isn't in that budget. Null means "do not split". */
export function catalogMaxPrice(budgetLabel, itemName) {
  const items = CATALOG[norm(budgetLabel)];
  const price = items ? items[norm(itemName)] : null;
  return price != null ? price : null;
}
`;

writeFileSync(OUT, out, 'utf8');
const counts = budgets.map((b) => `${b}: ${Object.keys(catalog[b]).length}`).join(', ');
console.log(`Wrote ${OUT}\n  ${counts}`);
